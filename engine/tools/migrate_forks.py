#!/usr/bin/env python3
"""migrate_forks.py — migrate the remaining med-agent forks into packs/.

One-shot but reproducible: for each fork it copies prompts/eval/knowledge,
extracts the routing table (engine/tools/extract_router.py), reads the OOB
KEYWORDS_* verbatim from the fork's bin/oob_check.sh (no transcription), and
emits packs/<name>/pack.yaml. Bespoke per-fork data (blocklist order + guards,
output sections, model params) is declared in CONFIGS below.

Usage: python3 engine/tools/migrate_forks.py [pack_name ...]   (default: all)
"""
from __future__ import annotations

import os
import re
import shutil
import subprocess
import sys

import yaml

HERE = os.path.dirname(os.path.abspath(__file__))
ENGINE = os.path.dirname(HERE)
CORE = os.path.dirname(ENGINE)
FOCUS = os.path.dirname(CORE)


def read_keywords(fork: str) -> dict:
    """Parse KEYWORDS_<NAME>="..." from a fork's oob_check.sh (join \\-continuations)."""
    raw = open(os.path.join(fork, "bin/oob_check.sh"), encoding="utf-8").read()
    joined = re.sub(r"\\\n\s*", "", raw)
    return {m.group(1): m.group(2)
            for m in re.finditer(r'^KEYWORDS_(\w+)="([^"]*)"', joined, re.MULTILINE)}


def build_blocklist(kw: dict, spec: list) -> list:
    out = []
    for rule in spec:
        if "literal" in rule:
            pattern = rule["literal"]
        else:
            pattern = "|".join(kw[v] for v in rule["vars"])
        entry = {"type": rule["type"], "pattern": pattern}
        if rule.get("unless"):
            entry["unless"] = rule["unless"]
        if rule.get("unless_all"):
            entry["unless_all"] = rule["unless_all"]
        out.append(entry)
    return out


def copy_tree(src, dst):
    if os.path.isdir(src):
        shutil.copytree(src, dst, dirs_exist_ok=True)


def migrate(cfg: dict):
    fork = os.path.join(FOCUS, cfg["fork"])
    pack = os.path.join(CORE, "packs", cfg["name"])
    adv = cfg["injection"] == "yaml_stack"
    os.makedirs(os.path.join(pack, "prompts"), exist_ok=True)
    os.makedirs(os.path.join(pack, "eval", "results"), exist_ok=True)
    os.makedirs(os.path.join(pack, "knowledge"), exist_ok=True)

    # prompts
    copy_tree(f"{fork}/prompts/sections", f"{pack}/prompts/sections")
    shutil.copy(f"{fork}/prompts/system_base.md", f"{pack}/prompts/system_base.md")
    shutil.copy(f"{fork}/prompts/output_schema.md", f"{pack}/prompts/output_schema.md")
    shutil.copy(f"{fork}/prompts/oob_templates.md", f"{pack}/prompts/oob_templates.md")
    if "doctor" in cfg["manifest"]["audiences"]:
        shutil.copy(f"{fork}/prompts/system_doctor.md", f"{pack}/prompts/system_base_doctor.md")
        shutil.copy(f"{fork}/prompts/output_schema_doctor.md", f"{pack}/prompts/output_schema_doctor.md")
        shutil.copy(f"{fork}/prompts/oob_templates_doctor.md", f"{pack}/prompts/oob_templates_doctor.md")

    # eval
    for fn in ("gold.yaml", "oob_gold.yaml", "judge_prompt.md"):
        if os.path.isfile(f"{fork}/eval/{fn}"):
            shutil.copy(f"{fork}/eval/{fn}", f"{pack}/eval/{fn}")
    for fn in cfg.get("extra_eval", []):
        if os.path.isfile(f"{fork}/eval/{fn}"):
            shutil.copy(f"{fork}/eval/{fn}", f"{pack}/eval/{fn}")
    open(f"{pack}/eval/results/.gitkeep", "w").close()

    # knowledge: yaml_stack copies the live stack to knowledge/; sections_only
    # keeps source YAML as provenance under knowledge/source/.
    if adv:
        copy_tree(f"{fork}/knowledge", f"{pack}/knowledge")
    else:
        os.makedirs(f"{pack}/knowledge/source", exist_ok=True)
        copy_tree(f"{fork}/knowledge", f"{pack}/knowledge/source")

    # routing table
    res = subprocess.run(["python3", os.path.join(HERE, "extract_router.py"),
                          f"{fork}/bin/router.sh"], capture_output=True, text=True)
    if res.returncode != 0:
        raise RuntimeError(f"extract_router failed: {res.stderr}")
    open(f"{pack}/knowledge/category_index.yaml", "w", encoding="utf-8").write(res.stdout)
    sys.stderr.write(f"  {cfg['name']}: {res.stderr.strip()}\n")

    # ingest build tools (markdown ingest packs, e.g. stcc) → engine/tools/
    for tool in cfg.get("ingest_tools", []):
        if os.path.isfile(f"{fork}/bin/{tool}"):
            shutil.copy(f"{fork}/bin/{tool}", os.path.join(HERE, tool))

    # manifest
    kw = read_keywords(fork)
    m = dict(cfg["manifest"])
    m["oob"]["blocklist"] = build_blocklist(kw, cfg["blocklist"])
    with open(f"{pack}/pack.yaml", "w", encoding="utf-8") as f:
        f.write(f"# Generated by engine/tools/migrate_forks.py from {cfg['fork']}.\n")
        yaml.safe_dump(m, f, allow_unicode=True, sort_keys=False, width=10000)
    print(f"migrated {cfg['fork']} → packs/{cfg['name']}")


