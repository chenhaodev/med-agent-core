def lite_manifest(id_, name, title, kind, header, sections, citation,
                  exempt, model_name="deepseek-chat", **extra):
    feat = {"knowledge_injection": "sections_only", "page_traceable": False,
            "ingest": extra.get("ingest", "manual"), "deep_eval": False}
    man = {
        "id": id_, "name": name, "source": {"title": title, "kind": kind},
        "audiences": ["patient"], "features": feat,
        "output_schema": "prompts/output_schema.md", "sections_dir": "prompts/sections",
        "system_base": "prompts/system_base.md", "knowledge": "knowledge/source",
        "knowledge_header": header,
        "routing": {"map": "knowledge/category_index.yaml",
                    "fallback": extra["fallback"], "llm_fallback": True},
        "oob": {"templates": "prompts/oob_templates.md", "blocklist": []},
        "output": {"oob_exempt_pattern": exempt,
                   "patient": {"required_sections": sections}},
        "model": {"name": model_name, "patient": {"temperature": 0.3, "max_tokens": 1500}},
        "eval": {"gold": "eval/gold.yaml", "oob_gold": "eval/oob_gold.yaml",
                 "judge_prompt": "eval/judge_prompt.md", "pass_threshold": 0.85,
                 "baseline": {"recorded_from": f"{extra['fork_rel']} (pre-migration); fill before deleting"}},
    }
    if "knowledge_section_title" in extra:
        man["knowledge_section_title"] = extra["knowledge_section_title"]
    if citation is not None:
        man["output"]["patient"]["citation_pattern"] = citation
    if "citation_requires" in extra:
        man["output"]["patient"]["citation_requires"] = extra["citation_requires"]
    return man


