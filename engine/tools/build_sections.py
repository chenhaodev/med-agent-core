def format_criteria_list(criteria, max_items=6):
    """格式化 criteria 列表为 markdown bullets"""
    if not criteria:
        return "（无具体标准，参见原始协议）"
    items = criteria[:max_items]
    return "\n".join(f"  - {c}" for c in items)


MAX_PROTOCOLS_PER_SECTION = 8  # 防止 prompt 膨胀


def select_top_protocols(protocols, max_n=MAX_PROTOCOLS_PER_SECTION):
    """优先选 A/B 级有 criteria 的协议，再补 D 级代表，合计不超过 max_n 条。"""
    a_level = [p for p in protocols if p.get("triage_levels", {}).get("A", {}).get("criteria")]
    b_level = [p for p in protocols if p.get("triage_levels", {}).get("B", {}).get("criteria")
               and p not in a_level]
    others = [p for p in protocols if p not in a_level and p not in b_level]

    selected = []
    for pool in [a_level, b_level, others]:
        for p in pool:
            if len(selected) >= max_n:
                break
            if p not in selected:
                selected.append(p)
        if len(selected) >= max_n:
            break
    return selected


def build_section(category, protocols):
    """构建单个 section markdown 文件内容"""
    lines = [
        f"# {category} 分诊知识片段",
        "",
        f"本片段整合了 STCC 指南中 **{category}** 类别的分诊标准。",
        "回答时严格基于以下内容，并遵守输出格式规范。",
        "",
    ]

    for proto in protocols:
        zh = proto.get("topic_zh", "")
        source = proto.get("source_file", "")
        lines.append(f"## {zh}（{source}）")
        lines.append("")

        # 关键追问
        kq = proto.get("key_questions", [])
        if kq:
            lines.append(f"**关键追问**：{' / '.join(kq[:6])}")
            lines.append("")

        # 分级标准
        triage = proto.get("triage_levels", {})
        level_labels = {
            "A": "A级（立即呼救 → 拨打120）",
            "B": "B级（立即急诊）",
            "C": "C级（当天就医）",
            "D": "D级（居家观察）",
        }
        for level in ["A", "B", "C", "D"]:
            data = triage.get(level, {})
            criteria = data.get("criteria", [])
            action = data.get("action", "")
            if criteria:
                lines.append(f"**{level_labels[level]}**（{action}）")
                for c in criteria[:6]:
                    lines.append(f"  - {c}")
                lines.append("")

        # 家庭护理
        hc = proto.get("home_care", [])
        if hc:
            lines.append("**D级居家护理**")
            for h in hc[:4]:
                lines.append(f"  - {h[:100]}")
            lines.append("")

        # 回电标准
        cb = [c for c in proto.get("callback_criteria", []) if "【立即急诊】" not in c]
        if cb:
            lines.append("**回电/复诊标准**")
            for c in cb[:4]:
                lines.append(f"  - {c}")
            lines.append("")

        lines.append("---")
        lines.append("")

    # few-shot
    fs = FEW_SHOTS.get(category, "")
    if fs:
        lines.append(fs.strip())
        lines.append("")

    return "\n".join(lines)


def main():
    categories_done = 0
    for yaml_path in sorted(YAML_DIR.glob("*.yaml")):
        category = yaml_path.stem
        try:
            with open(yaml_path, encoding="utf-8") as f:
                data = yaml.safe_load(f)
        except Exception as e:
            print(f"  ✗ 读取 {yaml_path.name} 失败：{e}")
            continue

        protocols = data.get("protocols", [])
        if not protocols:
            print(f"  ⚠ {category}: 无 protocols，跳过")
            continue

        protocols = select_top_protocols(protocols)
        content = build_section(category, protocols)
        out_path = SECTIONS_DIR / f"{category}.md"
        out_path.write_text(content, encoding="utf-8")
        print(f"  ✓ {category}.md ({len(protocols)} 个协议, {len(content)} 字符)")
        categories_done += 1

    print(f"\n完成，共生成 {categories_done} 个 section 文件。")


if __name__ == "__main__":
    main()
